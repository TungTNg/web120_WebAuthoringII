 <h2 class="pageID">~Responsive Website vs Native Mobile App ~</h2>
 
<div id="content">
    
    <h3>Introduction</h3>
    <p>Do you remember the last time it took you a few seconds thinking whether or not should you use the mobile facebook website or the already 
    installed mobile app version on your iPhone to register for a new Tinder account? Do you actually ever notice any differences between those 2 
    complete different software architectures yet look and feel so much alike? It may not be crucial for you in those 5 seconds to decide which one 
    hat you will use, but for a lot of app developers and companies out there, this is a extremely difficult question that has been tackled for years, 
    for them trying to find an answer: Should they just build a universal responsive website that also works for different mobile/tablet screen sizes
    and call it a day, or should they pour money and efforts to release different mobile applications on multiple platforms/programming languages 
    (Java on Android’s Google Play and Objective-C for Apple’s iOS App Store)? 
    </p>
    
    <h3>The Rundown</h3>
    <p>In 2018, it’s hard to imagine that you haven’t ever use a smartphone or a tablet before, and if you indeed used, are using and will be using those 
    little modern gadgets, I think you pretty much have an idea about the pros/cons of using your favorite app with a responsive website in a browser vs opening 
    it via the natively installed mobile application.
    </p>
    
    <h3>< !-- The website --></h3>
    <h4>&nbsp;&nbsp; +Plus for the user….:</h4>
    <p>No need to wait for which could be a lengthy installation: Some big apps that require you to download more than 200 worth of mbs could take you more than 
    20 minutes to fully and completely install with your 3G speed from the app stores (Please, for the love of 2018 Internet God, cash out $40 and get an
    unlimited 4G data plan please, you 3G cheapass!!!). That nonsense doesn’t occur with the web version. For example, in 3 seconds you could open your 
    Chrome/Safari browser on your iPhone and type in the link: <a href="https://www.tungthecoder.com">www.tungthecoder.com</a> and see the end result immediately. 
    Voila, magic!!!!
    </p>
    
    <h4>&nbsp;&nbsp; and…. big <strong><u>PLUSES</u></strong> for developer:</h4>
    <p>Reduced time, money, and efforts to develop & maintain the website: I mean, it’s the same software architecture, says Desktops, mobile Phone, 
    fashion-ish tablets/2-in-1… it’s all gonna be the same HTML/CSS/Javascript with the same backend-ish frameworks (NodeJS, Express, php….) 
    You guys only have to tweak it a little bit so that small screen users can read your stuffs a little bit more easily without all the zooming 
    frustrating shits! Besides, you can see the end testing result immediately or release it as soon as the product is ready to be deployed 
    (without weeks of waiting for approval from either Apple’s App Store of Google Play store) and no guideline needed to follow or financial fees 
    are required compare to if you choose to release the app in these stores (Goran, 2017). 
    </p>
    <p>With these pros, a lot of small budget developers like myself will often opt to go with releasing the responsive mobile website for the app first. 
    But keep in mind, this path has some limitations that are in opposite the advantages for choosing to develop the app natively for multiple mobile platforms.
    </p>
    
    <h3>// The native app:</h3>
    <p>Another online article that I found on thinkmobiles.com also help me to understand why big name app developing companies are fond of choosing 
    to go with the native app approach: 
        <ul>
            <li>More niche features and often have user-friendly design: Do you believe in one-size fit all shoes/underwears/bras? Exactly… 
            for ALOT of users, they usually DON’T! They really really really want that push notification, that fingerprint-pattern login, that swipe 
            to go back features that often time universal responsive website app version doesn’t have. </li>
            <li>No internet? No problem!: If you’re on a road trip to a far away land that has no internet access, you probably already pre-downloaded 
            all of your favorite songs or movies that you want to watch with your native apps (spotify, netflix, youtube…) to watch them later on in offline mode. 
            Can you do the same with the responsive websites in Chrome or Safari? Oh no!.... I know… that error “there is no Internet Connection” is freakin’ ugly~~</li>
            <li>More special features and functions: Didn’t I already cover this thing above? Yasss queen, I did!!! However, I just want to emphasize the 
            same important point again: the web version of your favorite app may let you to see the fabulous images, but it may not let you to upload them 
            (Instagram).... It may let you hear audio & sounds, but it may not let you speak with other person using your phone mic (Twitch)... It may help 
            you to find a route you specify from 2 points with street addresses, but it can’t access to your current GPS point (Google Map).... and most importantly,
            mobile web version performance is often time much slower/more sluggish compared to its counterpart, the mobile native app.</li>
        </ul>
    </p>
    
    <h3>The Conclusion()</h3>
    <p>Whether should you choose to deploy your app on a responsive website platform or a native mobile application, you should consider the advantages 
    of each method and how much effort/budget it requires you to put into the development process. If your resources are limited, you may choose to go 
    with the web approach initially as this is what I am doing. However, your ultimate goal still is to create both of them in the end as both have some
    very unique characteristics and they can together help to spread your app recognition on multiple platforms,  across desktop users and mobile users.
    </p>
